export const getLanguage = (language: LanguageCode) =>
  languages.find((item) => item.code === language) ?? languages[0];

export const isLanguageCode = (value: string): value is LanguageCode =>
  languages.some((language) => language.code === value);

export const createTranslator = (language: LanguageCode): Translator => {
  const dictionary = translations[language];

  return (key, values) =>
    dictionary[key].replace(/\{(\w+)\}/g, (match: string, token: string) =>
      values?.[token] === undefined ? match : String(values[token]),
    );
};
